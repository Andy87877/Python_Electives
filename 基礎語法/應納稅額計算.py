# 全年應納稅額 = 綜合所得淨額 x 稅率 - 累進差額
m = eval(input("請輸入所得: "))
n = 0 #應納稅額
if (m <= 540000):
    n = m*0.05
elif (m <= 1210000):
    n = m*0.12
    n -= 37800
elif (m <= 2420000):
    n = m*0.20
    n -= 134600
elif (m <= 453000):
    n = m*0.30
    n -= 376600
else:
    n = m*0.40
    n -= 829600
print("應納稅額為:",n)